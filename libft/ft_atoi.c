#include <stdlib.h>

int	ft_atoi(char *str)
{
	int rt;
	int neg;

	rt = 0;
	neg = 1;
	while (*str == '\t' || *str == '\v' || *str == '\r' || *str == '\f'
			|| *str == ' ' || *str == '\n')
		str++;
	if (*str == '-')
	{
		neg = -1;
		str++;
	}
	if (*str == '+' && neg == -1)
		return (0);
	if (*str == '+')
		str++;
	while (str != '\0' && *str >= '0' && *str <= '9')
	{
		rt *= 10;
		rt += *str - '0';
		str++;
	}
	return (rt * neg);
}
