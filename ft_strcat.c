#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	size_t	a;
	size_t	b;

	a = 0;
	b = 0;
	while (dest[a] != '\0')
		a++;
	while (src[b] != '\0')
		dest[a++] = src[b++];
	dest[a] = '\0';
	return (dest);
}
